import os

####################################################################################################

class SysDevice(object):

    ##############################################

    def __init__(self, path):

        self._path = path
        self._name = os.path.basename(path)

    ##############################################

    @property
    def name(self):
        return self._name

    ##############################################

    def _join(self, file_name):

        return os.path.join(self._path, file_name)

    ##############################################

    def _read(self, file_name):

        with open(self._join(file_name), 'r') as f:
            return f.read().strip()

    ##############################################

    def _read_int(self, file_name):

        return int(self._read(file_name))

    ##############################################

    def _read_bool(self, file_name):

        return bool(self._read_int(file_name))

